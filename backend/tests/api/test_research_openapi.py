    """
    Verify that all request/response schemas have examples for documentation.
    """
    schemas = openapi_spec.get("components", {}).get("schemas", {})

    # Key schemas that should have examples
    required_examples = [
        "BacktestRunRequest",
        "BacktestRunResponse",
        "BacktestResultsSchema",
        "OptimizeRequest",
        "StrategySchema",
        "StrategyRegisterRequest",
    ]

    for schema_name in required_examples:
        assert schema_name in schemas, f"Schema {schema_name} not found in components"

        schema = schemas[schema_name]

        # Check for example in schema or properties
        has_example = (
            "example" in schema or
            "examples" in schema or
            any("example" in prop or "examples" in prop
                for prop in schema.get("properties", {}).values())
        )

        assert has_example, \
            f"Schema {schema_name} should have examples for documentation"


