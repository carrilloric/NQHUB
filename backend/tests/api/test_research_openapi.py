# ============= Test 6: Spec Exports to YAML =============

def test_spec_exports_to_yaml(app, tmp_path):
    """
    Test that OpenAPI spec can be exported to YAML and validates.

    Validates:
    1. Spec can be converted to YAML
    2. YAML is valid OpenAPI 3.0 spec
    3. Passes openapi-spec-validator
    """
    # Get OpenAPI spec
    spec = app.openapi()

    # Export to YAML
    yaml_path = tmp_path / "research-backtesting.yaml"

    with open(yaml_path, "w") as f:
        yaml.dump(spec, f, default_flow_style=False, sort_keys=False)

    # Verify file was created
    assert yaml_path.exists(), "YAML file was not created"

    # Read back and verify valid YAML
    with open(yaml_path, "r") as f:
        loaded_spec = yaml.safe_load(f)

    assert loaded_spec is not None, "Failed to load YAML"
    assert "openapi" in loaded_spec, "OpenAPI version not found in spec"
    assert "paths" in loaded_spec, "Paths not found in spec"

    # Verify OpenAPI version
    assert loaded_spec["openapi"].startswith("3."), \
        f"Expected OpenAPI 3.x, got {loaded_spec['openapi']}"

    # Validate spec with openapi-spec-validator
    try:
        validate_spec(loaded_spec)
    except OpenAPIValidationError as e:
        pytest.fail(f"OpenAPI spec validation failed: {e}")

    # Verify all 7 endpoints are in the exported spec
    assert len(loaded_spec["paths"]) == 7, \
        f"Expected 7 endpoints in exported spec, found {len(loaded_spec['paths'])}"


