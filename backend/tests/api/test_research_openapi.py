# ============= Test 2: Backtest Run Accepts Correct Body =============

def test_backtest_run_accepts_correct_body(openapi_spec):
    """
    Test that POST /backtest/run accepts the correct request body schema.

    Required fields:
    - strategy_id: string
    - timeframe: string
    - start_date: date
    - end_date: date
    - commission: float (default 0.0002)
    - slippage: float (default 0.0001)
    - initial_capital: float (default 25000.0)
    - parameters: dict (default {})
    """
    endpoint = "/research/backtest/run"
    spec = openapi_spec["paths"][endpoint]["post"]

    # Verify request body exists
    assert "requestBody" in spec, "Request body not defined for /backtest/run"

    request_body = spec["requestBody"]["content"]["application/json"]["schema"]

    # Get schema reference and resolve it
    if "$ref" in request_body:
        schema_name = request_body["$ref"].split("/")[-1]
        schema = openapi_spec["components"]["schemas"][schema_name]
    else:
        schema = request_body

    # Verify required fields
    required_fields = ["strategy_id", "timeframe", "start_date", "end_date"]
    assert "required" in schema, "Required fields not specified in schema"

    for field in required_fields:
        assert field in schema["required"], \
            f"Field '{field}' should be required in BacktestRunRequest"

    # Verify properties
    properties = schema["properties"]

    assert "strategy_id" in properties
    assert properties["strategy_id"]["type"] == "string"

    assert "timeframe" in properties
    assert properties["timeframe"]["type"] == "string"

    assert "initial_capital" in properties
    assert properties["initial_capital"]["type"] == "number"
    # Verify default is Apex $25K
    assert properties["initial_capital"].get("default") == 25000.0, \
        "initial_capital default should be 25000.0 (Apex account)"

    assert "commission" in properties
    assert properties["commission"]["type"] == "number"
    assert properties["commission"].get("default") == 0.0002

    assert "slippage" in properties
    assert properties["slippage"]["type"] == "number"
    assert properties["slippage"].get("default") == 0.0001

    assert "parameters" in properties
    assert properties["parameters"]["type"] == "object"


